/**
 * Get all colleges for a given state
 */
export function getCollegesByState(state: string): string[] {
  // Try exact match first
  if (collegesByState[state]) {
    return collegesByState[state];
  }
  
  // Try case-insensitive match
  const stateKey = Object.keys(collegesByState).find(
    key => key.toLowerCase() === state.toLowerCase()
  );
  
  return stateKey ? collegesByState[stateKey] : [];
}

/**
 * Search colleges within a state
 */
export function searchColleges(state: string, query: string): string[] {
  const colleges = getCollegesByState(state);
  
  if (!query.trim()) {
    return colleges;
  }
  
  const lowerQuery = query.toLowerCase();
  return colleges.filter(college => 
    college.toLowerCase().includes(lowerQuery)
  );
}

export function getAvailableStates(): string[] {
  return Object.keys(collegesByState).sort();
}

/**
 * Get all colleges from all states
 */
export function getAllColleges(): string[] {
  const allColleges: string[] = [];
  for (const state of Object.keys(collegesByState)) {
    allColleges.push(...collegesByState[state]);
  }
  return allColleges.sort();
}

/**
 * Search across all colleges from all states
 */
export function searchAllColleges(query: string): string[] {
  const colleges = getAllColleges();
  
  if (!query.trim()) {
    return colleges;
  }
  
  const lowerQuery = query.toLowerCase();
  return colleges.filter(college => 
    college.toLowerCase().includes(lowerQuery)
  );
}
