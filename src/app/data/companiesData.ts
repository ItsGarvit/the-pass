/**
 * Get all companies for a given state
 */
export function getCompaniesByState(state: string): string[] {
  // Try exact match first
  if (companiesByState[state]) {
    return companiesByState[state];
  }
  
  // Try case-insensitive match
  const stateKey = Object.keys(companiesByState).find(
    key => key.toLowerCase() === state.toLowerCase()
  );
  
  return stateKey ? companiesByState[stateKey] : [];
}

/**
 * Search companies within a state
 */
export function searchCompanies(state: string, query: string): string[] {
  const companies = getCompaniesByState(state);
  
  if (!query.trim()) {
    return companies;
  }
  
  const lowerQuery = query.toLowerCase();
  return companies.filter(company => 
    company.toLowerCase().includes(lowerQuery)
  );
}

export function getAvailableStatesForCompanies(): string[] {
  return Object.keys(companiesByState).sort();
}

/**
 * Get all companies from all states
 */
export function getAllCompanies(): string[] {
  const allCompanies: string[] = [];
  for (const state of Object.keys(companiesByState)) {
    allCompanies.push(...companiesByState[state]);
  }
  return allCompanies.sort();
}

/**
 * Search across all companies from all states
 */
export function searchAllCompanies(query: string): string[] {
  const companies = getAllCompanies();
  
  if (!query.trim()) {
    return companies;
  }
  
  const lowerQuery = query.toLowerCase();
  return companies.filter(company => 
    company.toLowerCase().includes(lowerQuery)
  );
}
